import time
import os
import msvcrt

def enter_up(text):
    numLines = text.count('\n')
    for n in range(numLines, -1, -1):
        os.system('cls')
        output = text
        for i in range(n):
            output = output[:output.rfind('\n')]
            output = "\n" + output
        print(output)
        time.sleep(0.06)
    return;

def exit_up(text):
    numLines = text.count('\n')
    for n in range(0, numLines + 1):
        os.system('cls')
        output = text
        for i in range(n):
            output = output[(output.find('\n') + 1):]
            output = output + "\n"
        print(output)
        time.sleep(0.06)
    return;

